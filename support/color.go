package support

import (
	"os"

	"github.com/mattn/go-isatty"
)

func IsColorSupported() bool {
	noColor := os.Getenv("NO_COLOR") != "" ||
		os.Getenv("TERM") == "dumb" ||
		(!isatty.IsTerminal(os.Stdout.Fd()) && !isatty.IsCygwinTerminal(os.Stdout.Fd()))
	return !noColor
}
